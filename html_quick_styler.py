def save_files_and_zip(index_html: str, css: str) -> Tuple[str, str, str]:
    tmpd = tempfile.mkdtemp(prefix="quick_styler_")
    index_path = os.path.join(tmpd, "index.html")
    css_path = os.path.join(tmpd, "styles.css")
    with open(index_path, "w", encoding="utf-8") as f: f.write(index_html)
    with open(css_path, "w", encoding="utf-8") as f: f.write(css)
    zip_path = os.path.join(tmpd, "site.zip")
    with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as z:
        z.write(index_path, "index.html")
        z.write(css_path, "styles.css")
    return index_path, css_path, zip_path

def build_page(title: str, description: str, palette_key: str, use_llm: bool, hf_token: Optional[str]):
    palettes = ColorPaletteGenerator()
    pal = palettes.get(palette_key)
    css = generate_css(pal)

    gh = GraniteHelper(hf_token if use_llm else None)
    h1, tag = gh.hero(title)
    feats = gh.bullets(title or "this product", n=3)
    about_text = gh.short_para("About this page: " + (title or "project"), 40)

    sections = detect_sections(description)
    parts = [
        f'<header class="sticky"><div class="nav"><div class="brand">{title or "Quick‑Styler"}</div><a class="cta" href="#contact">Contact</a></div></header>',
    ]
    if "hero" in sections: parts.append(hero_section(h1, tag))
    if "features" in sections: parts.append(features_section(feats))
    if "about" in sections: parts.append(about_section(about_text))
    if "portfolio" in sections: parts.append(portfolio_section())
    if "testimonials" in sections: parts.append(testimonials_section())
    if "pricing" in sections: parts.append(pricing_section())
    if "contact" in sections: parts.append(contact_section())
    if "footer" in sections: parts.append(footer_section(title or "Quick‑Styler"))

    html_preview = build_preview_html(title, description, css, parts)
    html_index   = build_index_html_external_css(title, description, parts)
    return html_preview, html_index, css, sections

