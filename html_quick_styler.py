# ---------- Section detection ----------
SECTION_KEYWORDS = {
    "hero": ["hero", "banner", "headline", "welcome", "cta", "call to action"],
    "features": ["feature", "benefit", "capability", "why", "value"],
    "about": ["about", "bio", "story", "mission", "vision"],
    "portfolio": ["portfolio", "work", "projects", "gallery"],
    "testimonials": ["testimonial", "reviews", "feedback", "social proof"],
    "pricing": ["pricing", "plan", "plans", "subscription", "package"],
    "contact": ["contact", "reach", "email", "form", "inquiry"],
    "footer": ["footer", "copyright", "policy", "links"],
}
def detect_sections(description: str) -> List[str]:
    t = (description or "").lower()
    found = [sec for sec, kws in SECTION_KEYWORDS.items() if any(k in t for k in kws)]
    if not found:
        found = ["hero", "features", "about", "contact", "footer"]
    if "footer" not in found:
        found.append("footer")
    return found

# ---------- Optional Granite helper ----------
class GraniteHelper:
    def __init__(self, token: Optional[str], model_id: str = "ibm-granite/granite-3.3-2b-instruct"):
        self.token = token.strip() if token else None
        self.model_id = model_id
        self.client = InferenceClient(token=self.token) if self.token else None
    def enabled(self) -> bool:
        return self.client is not None
    def infer(self, prompt: str, max_new_tokens: int = 160, temperature: float = 0.7) -> str:
        if not self.enabled():
            return ""
        try:
            out = self.client.text_generation(
                model=self.model_id, prompt=prompt,
                max_new_tokens=max_new_tokens, temperature=temperature,
                repetition_penalty=1.05, stream=False,
            )
            return (out or "").strip()
        except Exception as e:
            print("Granite error:", e)  # keep running
            return ""
    def hero(self, title: str) -> Tuple[str, str]:
        default_head = title or "Welcome"
        default_tag = "Build something remarkable."
        if not self.enabled():
            return default_head, default_tag
        txt = self.infer(
            f'Create JSON: {{"h1":"(<=6 words)", "tagline":"(<=14 words)"}} '
            f'for landing page titled "{title}".'
        )
        try:
            data = json.loads(re.search(r"\{.*\}", txt, re.S).group(0))
            return data.get("h1", default_head), data.get("tagline", default_tag)
        except Exception:
            return default_head, default_tag
    def bullets(self, topic: str, n: int = 3) -> List[str]:
        if not self.enabled():
            return ["Fast to start", "Beautifully styled", "Mobile‑ready"][:n]
        txt = self.infer(f"List {n} short feature bullets (<=8 words) for {topic}. Return JSON array.")
        try:
            arr = json.loads(re.search(r"\[.*\]", txt, re.S).group(0))
            return [str(x) for x in arr][:n]
        except Exception:
            return ["Fast to start", "Beautifully styled", "Mobile‑ready"][:n]
    def short_para(self, topic: str, words: int = 40) -> str:
        if not self.enabled():
            return "We craft thoughtful experiences with a focus on clarity, speed, and delightful design."
        txt = self.infer(f"Write a {words}-word paragraph about: {topic}.", max_new_tokens=words*2, temperature=0.8)
        return txt or "We craft thoughtful experiences with a focus on clarity, speed, and delightful design."

